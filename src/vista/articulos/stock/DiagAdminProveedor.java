    private void modificarProveedor() {
  
        if (tblProveedores.getSelectedRow() != -1) {
            Proveedor proveedor = ProveedorFacade.getInstance().buscar((Long) tblProveedores.getValueAt(tblProveedores.getSelectedRow(), 0));
            System.out.println("DEPOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOOSITO------->" +proveedor);
            DiagProveedor diagAdmProveedor = new DiagProveedor(null, true, "Modificaci√≥n", proveedor);
            diagAdmProveedor.setLocation(Comunes.centrarDialog(diagAdmProveedor));
            diagAdmProveedor.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Proveedor");
        }

    }

    private void eliminarProveedor() {
        if (tblProveedores.getSelectedRow() != -1) {
            int i = JOptionPane.showConfirmDialog(this, "¬øDesea eliminar el Registro seleccionado?", "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
            if (i == 0) {
                ProveedorFacade.getInstance().eliminar((Long) tblProveedores.getValueAt(tblProveedores.getSelectedRow(), 0));
                JOptionPane.showMessageDialog(null, "Registro eliminado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Proveedor");
        }
    }

