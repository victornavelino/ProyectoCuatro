    private void cargarTablaProveedores(List<Proveedor> proveedores) {
        modeloTablaProveedores = new ModeloTablaNoEditable();
        cargarEncabezadosTablaProveedores(modeloTablaProveedores);
        configurarTabla(tblProveedores);
        try {
            cargarProveedores(proveedores);
        } catch (Exception ex) {

        }
    }

    private void cargarEncabezadosTablaProveedores(ModeloTablaNoEditable modeloTablaPersonas) {
        modeloTablaPersonas.addColumn("Id");
        modeloTablaPersonas.addColumn("Descripci√≥n");

        tblProveedores.setModel(modeloTablaPersonas);
        Comunes.setOcultarColumnasJTable(tblProveedores, 0);
    }

    private void cargarProveedores(List<Proveedor> proveedores) {
        try {
            modeloTablaProveedores = new ModeloTablaNoEditable();
            cargarEncabezadosTablaProveedores(modeloTablaProveedores);
            for (Proveedor proveedor : proveedores) {
                cargarProveedor(proveedor);
            }

            tblProveedores.setModel(modeloTablaProveedores);
        } catch (Exception ex) {
            Logger.getLogger(DiagAdminProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarProveedor(Proveedor proveedor) {

        Object[] fila = new Object[5];
        fila[0] = proveedor.getId();
        fila[1] = proveedor.getDescripcion();

        modeloTablaProveedores.addRow(fila);
    }

    private void configurarTabla(JTable tbl) {
        JViewport scroll = (JViewport) tbl.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tbl.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tbl.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (1 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                case 3:
                case 4:
                case 5:
                case 6:
                case 7:
                case 8:
                case 9:
                case 10:
                case 11:
                case 12:
                    anchoColumna = (5 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
            tbl.setColumnModel(modeloColumna);
        }
        tbl.getTableHeader().setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
        tbl.getTableHeader().setBackground(java.awt.Color.WHITE);
        tbl.getTableHeader().setForeground(Color.BLACK);
        //Si le queremos cambiar el tama√±o a la tablita
        tbl.setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
    }

