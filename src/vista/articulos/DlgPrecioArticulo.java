    private void inicializarComponentes() {
        Comunes.limitarTextField(tfprecio, 30);
        this.setTitle("Articulo");
        Comunes.cargarJCombo(cboListaPrecios, ListaPrecioFacade.getInstance().getTodos());
        Comunes.cargarJCombo(cboSucursal, SucursalFacade.getInstance().getTodos());
        
        //Comunes.cargarJCombo(cboUnidadDeMedida, UnidadMedidaFacade.getInstance().getTodos());
        if (tipoOperacion.equals("Modificación")) {
            cargarDatosArticulo();
        }
        else{tfDescripcion.setText(articulo.getDescripcion());}
    }

    private void aceptar() {
        if (tipoOperacion.equals("Alta")) {
            if (validarArticulo()) {
                precioArticulo = new PrecioArticulo();
                precioArticulo.setArticulo(articulo);
                precioArticulo.setListaPrecio((ListaPrecio)cboListaPrecios.getSelectedItem());
                precioArticulo.setSucursal((Sucursal)cboSucursal.getSelectedItem());
                precioArticulo.setPrecio(redondear(new BigDecimal(tfprecio.getText())));
                
                PrecioArticuloFacade.getInstance().alta(precioArticulo);
                JOptionPane.showMessageDialog(this, "Precio de Articulo agregado!");
                this.dispose();

            }

        } else if (tipoOperacion.equals("Modificación")) {
            if (validarArticulo()) {
                //articulo.setCodigoBarra(tfCodigoBarra.getText().toUpperCase());
               
                precioArticulo.setListaPrecio((ListaPrecio)cboListaPrecios.getSelectedItem());
                precioArticulo.setSucursal((Sucursal)cboSucursal.getSelectedItem());
                precioArticulo.setPrecio(redondear(new BigDecimal(tfprecio.getText())));
                PrecioArticuloFacade.getInstance().modificar(precioArticulo);
                JOptionPane.showMessageDialog(this, "Precio de Articulo modificado!");
                this.dispose();

            }
        }

    }

